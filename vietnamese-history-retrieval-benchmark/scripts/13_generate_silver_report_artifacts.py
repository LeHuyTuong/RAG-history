def write_latex_table(metrics: pd.DataFrame, output: Path) -> None:
    if metrics.empty:
        output.write_text("% TBD\n", encoding="utf-8")
        return
    table = metrics.copy()
    for col in METRIC_COLS:
        table[col] = pd.to_numeric(table[col], errors="coerce")
    output.write_text(
        "% Preliminary silver-label pilot results. Labels are silver and not manually verified.\n"
        + table.to_latex(index=False, float_format="%.4f"),
        encoding="utf-8",
    )


def top_results_summary(retrieval: pd.DataFrame, qid: str) -> str:
    rows = []
    subset = retrieval[retrieval["qid"] == qid]
    for method, group in subset.groupby("method", sort=True):
        top = group.sort_values("rank").head(3)
        items = [
            f"{int(row['rank'])}:{row['chunk_id']}@p{row['page_number']} rel={row['is_relevant']}"
            for row in top.to_dict("records")
        ]
        rows.append(f"{method} -> " + "; ".join(items))
    return " | ".join(rows)


def write_manual_audit(questions: pd.DataFrame, qrels: pd.DataFrame, per_query: pd.DataFrame, retrieval: pd.DataFrame, output: Path) -> None:
    q_by_id = {row["qid"]: row for row in questions.to_dict("records")}
    positives = qrels[qrels["relevance"] == "1"].groupby("qid").first().reset_index()
    positive_by_qid = {row["qid"]: row for row in positives.to_dict("records")}
    pivot = per_query.pivot_table(index="qid", columns="method", values="hit_at_5", aggfunc="first").fillna("false")

    chosen: list[str] = []
    random_qids = questions.sample(n=min(10, len(questions)), random_state=42)["qid"].tolist()
    chosen.extend(random_qids)
    if {"hybrid_e5", "bm25"}.issubset(pivot.columns):
        chosen.extend(pivot[(pivot["hybrid_e5"] == "true") & (pivot["bm25"] != "true")].head(5).index.tolist())
        chosen.extend(pivot[(pivot["bm25"] == "true") & (pivot["hybrid_e5"] != "true")].head(5).index.tolist())
    hard = per_query[per_query["error_type"] != "no_error"]["qid"].drop_duplicates().head(5).tolist()
    chosen.extend(hard)
    if len(dict.fromkeys(chosen)) < 25:
        chosen.extend(questions["qid"].tolist())
    chosen = list(dict.fromkeys(chosen))[:25]

    rows = []
    for qid in chosen:
        q = q_by_id.get(qid, {})
        positive = positive_by_qid.get(qid, {})
        rows.append(
            {
                "qid": qid,
                "question": q.get("question", ""),
                "expected_answer": q.get("proposed_answer", ""),
                "silver_evidence_chunk": positive.get("chunk_id", ""),
                "silver_page": positive.get("page_number", ""),
                "top_results_by_method": top_results_summary(retrieval, qid),
                "audit_decision": "",
                "human_note": "",
            }
        )
    write_csv(pd.DataFrame(rows), output)


def write_manual_audit_instructions(output: Path) -> None:
    output.write_text(
        "# Manual Audit Instructions\n\n"
        "Audit `data/benchmark/manual_audit_sample.csv` before relying on the preliminary evaluation.\n\n"
        "1. Open each row and read the question plus expected answer.\n"
        "2. Open the silver evidence chunk/page in the original PDF or `chunks.csv`.\n"
        "3. Mark `audit_decision=accept` only if the chunk directly supports the answer.\n"
        "4. Mark `audit_decision=edit_question` if the question is useful but vague or noisy.\n"
        "5. Mark `audit_decision=reject` if the answer is unsupported, too generic, or caused by OCR/extraction noise.\n"
        "6. For no-answer rows, search the corpus before accepting `relevance=NONE`.\n"
        "7. Add a concise `human_note` explaining the decision.\n\n"
        "After audit, copy accepted rows into final `questions.csv` and `qrels.csv`, then set `verified=true` only for rows you manually checked.\n",
        encoding="utf-8",
    )


def main() -> None:
    metrics = read_csv(ROOT / "results" / "metrics.csv")
    status = read_csv(ROOT / "results" / "baseline_run_status.csv")
    per_query = read_csv(ROOT / "results" / "per_query_analysis.csv")
    retrieval = read_csv(ROOT / "results" / "retrieval_results.csv")
    questions = read_csv(ROOT / "data" / "benchmark" / "silver_questions.csv")
    qrels = read_csv(ROOT / "data" / "benchmark" / "silver_qrels.csv")

