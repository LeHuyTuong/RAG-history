METRIC_COLS = [
    "recall_at_5",
    "recall_at_10",
    "mrr_at_10",
    "evidence_hit_rate_at_5",
    "citation_accuracy_at_5",
    "no_answer_accuracy",
]


def fmt(value: object) -> str:
    try:
        return f"{float(value):.4f}"
    except Exception:
        return "TBD"


def metrics_markdown(metrics: pd.DataFrame) -> str:
    display = metrics.copy()
    for col in METRIC_COLS:
        display[col] = display[col].map(fmt)
    return display.to_markdown(index=False)


def best_method(metrics: pd.DataFrame) -> tuple[str, str]:
    if metrics.empty:
        return "TBD", "TBD"
    scores = metrics.copy()
    for col in METRIC_COLS:
        scores[col] = pd.to_numeric(scores[col], errors="coerce")
    primary = ["recall_at_5", "recall_at_10", "mrr_at_10"]
    aggregate = scores[primary].mean(axis=1)
    idx = aggregate.idxmax()
    return str(scores.loc[idx, "method"]), f"{float(aggregate.loc[idx]):.4f}"


def write_method_comparison(metrics: pd.DataFrame, status: pd.DataFrame, output: Path) -> None:
    method, aggregate = best_method(metrics)
    if method == "hybrid_e5":
        result_sentence = "Hybrid retrieval achieved the best score on the silver-label pilot set."
    elif method == "TBD":
        result_sentence = "Metrics are TBD."
    else:
        result_sentence = f"{method} achieved the best aggregate primary score on the silver-label pilot set."

    status_md = status.to_markdown(index=False) if not status.empty else "TBD"
    output.write_text(
        "# Method Comparison\n\n"
        "This is a preliminary evaluation on a silver-label pilot benchmark. "
        "The qrels were generated by an LLM-assisted labeling process and are not manually verified labels.\n\n"
        "## Metrics\n\n"
        + metrics_markdown(metrics)
        + "\n\n## Best Method\n\n"
        + result_sentence
        + f"\n\nAggregate primary score used for this summary: `{aggregate}` over Recall@5, Recall@10, and MRR@10.\n\n"
        "Do not generalize this result beyond the local 15-PDF corpus.\n\n"
        "## Baseline Status\n\n"
        + status_md
        + "\n",
        encoding="utf-8",
    )


def write_error_analysis(per_query: pd.DataFrame, output: Path) -> None:
    counts = per_query["error_type"].value_counts().reset_index()
    counts.columns = ["error_type", "count"]
    by_method = per_query.groupby(["method", "error_type"]).size().reset_index(name="count")
    examples = per_query[per_query["error_type"] != "no_error"].head(12)
    example_lines = []
    for row in examples.to_dict("records"):
        example_lines.append(
            f"- `{row['qid']}` / `{row['method']}` / `{row['question_type']}`: "
            f"{row['error_type']} | {row['question']}"
        )

    output.write_text(
        "# Error Analysis\n\n"
        "This analysis is based on silver_qrels from an LLM-assisted labeling process. "
        "It is suitable for pilot debugging, not final claims.\n\n"
        "## Error Counts\n\n"
        + counts.to_markdown(index=False)
        + "\n\n## Error Counts By Method\n\n"
        + by_method.to_markdown(index=False)
        + "\n\n## Observations\n\n"
        "- Missed date/year questions usually occur when OCR/extraction noise, noisy years, or page/chunk context weakens lexical matching.\n"
        "- Missed named-entity questions often involve imperfect entity extraction from Vietnamese PDF text.\n"
        "- Semantic mismatch is common for dense retrieval when the generated question is broader than the silver evidence sentence.\n"
        "- OCR/extraction noise remains a corpus limitation because D003 and D007 have no usable extracted text without OCR.\n"
        "- Chunking errors should be manually audited where the expected page appears but the silver chunk does not directly answer the question.\n"
        "- In this run, BM25 is stronger than E5 on the silver labels; Hybrid BM25+E5 recovers some lexical evidence but does not exceed BM25 on primary metrics.\n\n"
        "## Example Error Cases\n\n"
        + ("\n".join(example_lines) if example_lines else "No error cases.")
        + "\n",
        encoding="utf-8",
    )


